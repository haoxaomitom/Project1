    void delete() {
        String makh = (String) tbNhanvien.getValueAt(this.row, 0);
        String manv = txtMaNV.getText();
        if (manv.equals(usernv.getMaNV())) {
            MsgBox.alert(this, "Bạn không thể tự xóa chính bạn !");
        } else {
            if (MsgBox.confirm(this, "Bạn thực sự muốn xóa Nhân Viên có mã: " + txtMaNV.getText() + "này?")) {
                try {
                    dao.delete(makh);
                    tkdao.delete(manv);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xóa thành công!");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xóa thất bại!");
                }
            }
        }
    }

    void setForm(NhanVien nv) {
        txtMaNV.setText(nv.getMaNV());
        txtTenNV.setText(nv.getTenNV());
        cbRole.setSelectedItem(nv.getChucVu());
        txtLuong.setText(String.format("%.0f", nv.getLuong()));
        txtDate.setDate(nv.getNgaySinh());
        rdoNam.setSelected(nv.getGioiTinh());
        rdoNu.setSelected(!nv.getGioiTinh());
        txtDiaChi.setText(nv.getDiaChi());
        txtSoDT.setText(nv.getSoDT());
        txtEmail.setText(nv.getEmail());
    }

    NhanVien getForm() {
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNV.getText());
        nv.setTenNV(txtTenNV.getText());
        nv.setChucVu(String.valueOf(cbRole.getSelectedItem()));
        nv.setLuong(Double.parseDouble(txtLuong.getText()));
        nv.setNgaySinh(txtDate.getDate());
        nv.setGioiTinh(rdoNam.isSelected());
        nv.setDiaChi(txtDiaChi.getText());
        nv.setSoDT(txtSoDT.getText());
        nv.setEmail(txtEmail.getText());
        return nv;
    }

    void edit() {
        String manv = (String) tbNhanvien.getValueAt(this.row, 0);
        NhanVien nv = dao.selectById(manv);
        this.setForm(nv);
    }

    void selectCurrentRow() {
        tbNhanvien.getSelectionModel().setSelectionInterval(row, row);
    }

    void first() {
        this.row = 0;
        this.edit();
        selectCurrentRow();
    }

    void prev() {
        if (this.row > 0) {
            this.row--;
            this.edit();
            selectCurrentRow();
        }
    }

    void next() {
        if (this.row < tbNhanvien.getRowCount() - 1) {
            this.row++;
            this.edit();
            selectCurrentRow();
        }
    }

    void last() {
        this.row = tbNhanvien.getRowCount() - 1;
        this.edit();
        selectCurrentRow();
    }

    private int getRowByValue(JTable table, Object value, int column) {
        for (int i = 0; i < table.getRowCount(); i++) {
            if (table.getValueAt(i, column).equals(value)) {
                return i;
            }
        }
        return -1; // Trả về -1 nếu không tìm thấy giá trị trong bảng
    }

    private void performSearch() {
        String maSVToFind = txtTim.getText();
        if (maSVToFind.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã sản phẩm");
        } else {
            int columnIndex = 0; // Đặt columnIndex là 0 nếu muốn tìm kiếm trong cột Mã SV
            int rowIndex = getRowByValue(tbNhanvien, maSVToFind, columnIndex);
            if (rowIndex != -1) {
                // Di chuyển đến vị trí tìm thấy trong bảng
                tbNhanvien.getSelectionModel().setSelectionInterval(rowIndex, rowIndex);
                tbNhanvien.scrollRectToVisible(tbNhanvien.getCellRect(rowIndex, 0, true));
            } else {
                JOptionPane.showMessageDialog(this, "Student ID not found");
            }
        }
    }
