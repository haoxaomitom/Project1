    void insert() {
        NhanVien nv = this.getForm();
        String manv = txtMaNV.getText();
        String tennv = txtTenNV.getText();
        String luong = txtTenNV.getText();
        String ngaysinh = txtDate.getDateFormatString();
        String sdt = txtSoDT.getText();
        String email = txtEmail.getText();
        String diachi = txtDiaChi.getText();
        if (manv.isEmpty()) {
            MsgBox.alert(this, "Mã nhân viên không được để trống !");
            txtMaNV.requestFocus();
        } else if (tennv.isEmpty()) {
            MsgBox.alert(this, "Tên Nhân Viên không được để trống !");
            txtTenNV.getText();
        } else if (luong.isEmpty()) {
            MsgBox.alert(this, "Lương không được để trống !");
            txtLuong.getText();
        } else if (ngaysinh.isEmpty()) {
            MsgBox.alert(this, "Vui lòng chọn ngày sinh !");
            txtDate.requestFocus();
        } else if (sdt.isEmpty()) {
            MsgBox.alert(this, "Vui lòng nhập số điện thoại !");
            txtSoDT.requestFocus();
        } else if (sdt.length() != 10) {
            MsgBox.alert(this, "Chỉ hỗ trợ số điện thoại có 10 số !");
            txtSoDT.requestFocus();
        } else if (email.isEmpty()) {
            MsgBox.alert(this, "Vui lòng nhập email !");
            txtEmail.requestFocus();
        } else if (diachi.isEmpty()) {
            MsgBox.alert(this, "Vui lòng nhập địa chỉ !");
            txtDiaChi.requestFocus();
        }
        try {
            dao.insert(nv); // thêm mới
            this.fillTable(); // đỗ lại bảng
            this.clearForm(); // xóa trắng form
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }

